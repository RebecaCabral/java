package app;

public class Caluladora {

        public int Somar(int A, int B)
        {
            int somarAB;
            somarAB = A + B;
            return somarAB;
        }

        public int Diminiur(int A, int B)
        {
            int diminuirAB;
            diminuirAB = A - B;
            return diminuirAB;
        }
}